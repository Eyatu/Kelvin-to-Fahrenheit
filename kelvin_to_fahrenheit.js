//Created a constant, kelvin, which has been set to 293
const kelvin= 0
//Created a variable, celsius, which has been set to 273 less than kelvin
let celsius= kelvin - 273
//Created a variable, celsius, which has been set accordingly
let fahrenheit= celsius * (9/5) + 32
//Rounded down fahrenheit
fahrenheit= Math.floor(fahrenheit)
console.log(`The temperature is ${fahrenheit} degrees Fahrenheit`)

